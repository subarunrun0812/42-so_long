#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	len_s;
	size_t	i;
	char	*dst;

	if (!s)
		return (NULL);
	len_s = ft_strlen(s);
	if ((size_t)start > len_s)
		len = 0;
	if (len_s - start < len)
		len = len_s - start;
	dst = (char *)malloc(sizeof(char) * (len + 1));
	if (!dst)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = s[start + i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
